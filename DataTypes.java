package assignments;

public class DataTypes {
    static void printDataTypes(int a, long b, float c, double d, char e) {
        System.out.println(a);
        System.out.println(b);
        System.out.printf("%.2f", c);
        System.out.println();
        System.out.printf("%.4f", d);
        System.out.println();
        System.out.println(e);
    }
}
